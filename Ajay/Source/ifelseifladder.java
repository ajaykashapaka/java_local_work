package Source;
public class ifelseifladder 
{
    public static void main(String[] args) 
	{ 
		int a=20;
		if(a==10)
			System.out.println("a==10");
		else if(a==15)
			System.out.println("a==15");
		else if(a==19)
			System.out.println("a==19");
		else {
			System.out.println("not equal");
		}
	} 
}
